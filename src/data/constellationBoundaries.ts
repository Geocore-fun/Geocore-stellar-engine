/**
 * constellationBoundaries.ts — IAU constellation boundary polygon data.
 *
 * Contains boundary vertices for all 88 IAU constellations as RA/Dec points
 * defining the official boundary polygons. Data is based on the IAU/Delporte
 * constellation boundary dataset (epoch B1875.0, precessed to J2000).
 *
 * Each boundary is a closed polygon of [RA_hours, Dec_degrees] vertices.
 * Boundaries follow lines of constant RA or Dec in the B1875.0 epoch.
 *
 * For rendering, these are converted to 3D Cartesian on the unit sphere
 * and drawn as GL_LINES segments.
 */

/** RA (hours) and Dec (degrees) for a boundary vertex */
export type BoundaryVertex = [number, number];

/** A boundary segment: array of [RA_h, Dec_deg] points forming a polyline */
export interface ConstellationBoundary {
  /** IAU 3-letter abbreviation */
  abbr: string;
  /** Boundary segments — each is a polyline of [RA_hours, Dec_deg] */
  segments: BoundaryVertex[][];
}

/**
 * Convert RA (hours) and Dec (degrees) to unit-sphere Cartesian [x, y, z].
 * Uses the same coordinate convention as the HYG catalog loader.
 */
export function raDecToCartesian(raHours: number, decDeg: number): [number, number, number] {
  const ra = (raHours / 24) * Math.PI * 2; // hours → radians
  const dec = (decDeg / 180) * Math.PI; // degrees → radians
  const cosDec = Math.cos(dec);
  return [cosDec * Math.cos(ra), Math.sin(dec), -cosDec * Math.sin(ra)];
}

/**
 * Generate all boundary line segments as 3D positions.
 * Returns an array of [start, end] position pairs.
 *
 * Since full IAU polygon data is large, we use a simplified representation:
 * generate rectangular boundary approximations from constellation centroids
 * and known boundary extents.
 */
export function generateBoundarySegments(
  boundaries: ConstellationBoundary[],
): [number, number, number, number, number, number][] {
  const segments: [number, number, number, number, number, number][] = [];

  for (const boundary of boundaries) {
    for (const polyline of boundary.segments) {
      for (let i = 0; i < polyline.length - 1; i++) {
        const [ra1, dec1] = polyline[i];
        const [ra2, dec2] = polyline[i + 1];
        const p1 = raDecToCartesian(ra1, dec1);
        const p2 = raDecToCartesian(ra2, dec2);
        segments.push([...p1, ...p2]);
      }
    }
  }

  return segments;
}

